        <span slot="header"><b>H, L, E, Visas</b></span>
  
        <p>
            Non-immigrant work visas (H, L, O, P, Q) require a CIS-approved petition from a U.S. sponsor. Treaty-trader or treaty-investor visas (E1/E2) can be applied for directly by the individual as long as he or she is from a country with which the United States has a treaty.        </p><br>
        <p>
            The most common work-related visas are H-1B Visas and L1 Visas. The H-1B is a way to bring foreign-born professionals to the United States for a period of up to six years. A sponsor is required and the employment may only start up when the new employee is in the United States. The L1- Visa is for people working for an employer abroad for one year in a related business entity in a manager, executive or specialized knowledge staff capacity, and who will come to the United States to continue providing services for his or her employer. O visas may be available for those with extraordinary ability in the sciences, arts, business or athletics and P visas may be available for athletes or entertainers.         </p>
      </AccordionItem>

